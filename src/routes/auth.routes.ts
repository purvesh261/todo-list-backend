import { Router } from 'express';
import { signup, login } from '../controllers/auth.controller.js';
import { validationSchemas } from '../validators/validators.js';
import { validate } from '../middleware/validate.middleware.js';

const router = Router();

const validators = validationSchemas.auth;

/**
 * @swagger
 * /api/auth/signup:
 *   post:
 *     summary: Register a new user
 *     tags: [Authentication]
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             required:
 *               - email
 *               - password
 *             properties:
 *               email:
 *                 type: string
 *                 format: email
 *                 description: User's email address
 *               password:
 *                 type: string
 *                 format: password
 *                 description: User's password
 *     responses:
 *       201:
 *         description: User created successfully
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 message:
 *                   type: string
 *                   example: User created successfully
 *         headers:
 *           Set-Cookie:
 *             schema:
 *               type: string
 *               description: JWT token stored in HTTP-only cookie
 */
router.post('/signup', validate(validators.signup), signup);

/**
 * @swagger
 * /api/auth/login:
 *   post:
 *     summary: Login user
 *     tags: [Authentication]
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             required:
 *               - email
 *               - password
 *             properties:
 *               email:
 *                 type: string
 *                 format: email
 *                 description: User's email address
 *               password:
 *                 type: string
 *                 format: password
 *                 description: User's password
 *     responses:
 *       200:
 *         description: Login successful
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 message:
 *                   type: string
 *                   example: Logged in successfully
 *         headers:
 *           Set-Cookie:
 *             schema:
 *               type: string
 *               description: JWT token stored in HTTP-only cookie
 */
router.post('/login', validate(validators.login), login);

export default router;
